    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        ControlCategoria objcc = new ControlCategoria();
        int ncol;
        Object[] fila;
        
        listaCategorias = objcc.consultarCategoria();

        DefaultTableModel modelo = new DefaultTableModel();
        this.jTable2.setModel(modelo);
            
        modelo.addColumn("identificacion");
        modelo.addColumn("nombre");
        ncol = modelo.getColumnCount();
            
        for (int i = 0; i < listaCategorias.size(); i++) {
            fila = new Object[ncol];
            fila[0] = listaCategorias.get(i).getIdCategoriast();
            fila[1] = listaCategorias.get(i).getTipoCategoria();
            modelo.addRow(fila);
        }
                
    }//GEN-LAST:event_jButton1ActionPerformed

