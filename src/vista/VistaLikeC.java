        // TODO add your handling code here:
        
       int id =Integer.parseInt(jTextField1.getText());
        ControlLike objl= new ControlLike();
        int ncol;
        Object[] fila;
        listalikes= objl.consultarLikes(id);
        DefaultTableModel modelo = new DefaultTableModel();
        this.jTable1.setModel(modelo);
        
        modelo.addColumn("idLike");
        modelo.addColumn("IdPost");
        modelo.addColumn("Correo usuario");


        ncol = modelo.getColumnCount();

        //Object[] fila = new Object[ncol];
        for (int i = 0; i < listalikes.size(); i++) {
            fila = new Object[ncol];
            fila[0]=listalikes.get(i).getIdLike();
            fila[1]=listalikes.get(i).getIdpostf();
            fila[2]=listalikes.get(i).getCorreoUL();


            modelo.addRow(fila);
            
        }
