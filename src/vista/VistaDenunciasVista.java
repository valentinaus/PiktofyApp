    public void mostrar() {
        
      
        modelo.addColumn("id Denuncia");
        modelo.addColumn("Descripci√≥n");
        modelo.addColumn("id del post");
        modelo.addColumn("Remitente");
        modelo.addColumn("Denunciado");
       
        String sql="select * from verdenuncias;";
        ControlVistas cv= new ControlVistas();
        
        table1=cv.traervistasDenuncias(sql);
        
        Object [] fila = new Object[table1.getColumnCount()];
        for (int x=0; x < table1.getRowCount(); x++) {
          for (int y=0; y < table1.getColumnCount(); y++) {

              fila[y] = table1.getValueAt(x, y);
        }
          modelo.addRow(fila);
        
        }
        jTable1.setModel(modelo);
    
    }
