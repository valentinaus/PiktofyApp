    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        // TODO add your handling code here:

        String valor = String.valueOf(jComboBox1.getSelectedItem());
        String correocon = jTextField7.getText();
        boolean f;

        if (correocon.equals("")) {

            JOptionPane.showMessageDialog(rootPane, "Ingresa el correo del usuario");

        } else {
            if (valor.equals("Eliminar Usuario")) {

                ControlUsuario cu = new ControlUsuario();
                usuario borrarUsuario = new usuario(correocon);
                cu.BorrarUsuario(borrarUsuario);

                JOptionPane.showMessageDialog(null, "Se Borró el Usuario correctamente");

            } else if (valor.equals("Bloquear Usuario")) {

                ControlUsuario crlu = new ControlUsuario();
                listaUCon = crlu.consultarUsuarios(correocon);

                if (listaUCon.isEmpty()) {

                    JOptionPane.showMessageDialog(null, "El usuario no existe");

                } else {
                    usuario usubloq = new usuario();
                    usubloq = listaUCon.getFirst();
                    int estado2 = listaUCon.getFirst().getEstadoBloqueo();
                    int estado = 2;

                    if (estado2 == 1) {

                        usuario usubloq2 = new usuario(correocon, 2);

                        f = crlu.actualizarUsuarioEstado(usubloq2);
                        JOptionPane.showMessageDialog(rootPane, "Esta usuario se ha bloqueado");

                    } else if (estado2 == 2) {

                        JOptionPane.showMessageDialog(rootPane, "Esta usuario ya está bloqueado");

                    }

                }

            } else if (valor.equals("Desbloquear Usuario")) {

                ControlUsuario crlu = new ControlUsuario();
                listaUCon = crlu.consultarUsuarios(correocon);

                if (listaUCon.isEmpty()) {

                    JOptionPane.showMessageDialog(null, "El usuario no existe");

                } else {

                    usuario usubloq = new usuario();
                    usubloq = listaUCon.getFirst();
                    int estado2 = listaUCon.getFirst().getEstadoBloqueo();

                    if (estado2 == 2) {

                        usuario usubloq2 = new usuario(correocon, 1);

                        f = crlu.actualizarUsuarioEstado(usubloq2);
                        JOptionPane.showMessageDialog(rootPane, "Esta usuario se ha desbloqueado exitosamente");

                    } else if (estado2 == 1) {

                        JOptionPane.showMessageDialog(rootPane, "Esta usuario ya está desbloqueado");

                    }

                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Este usuario no existe");

            }
        }
        consultarUsuarios();

    }//GEN-LAST:event_jButton17ActionPerformed

